def my_sum(num1, num2):
    total = num1 + num2
    return total

# num1 = 2
# num2 = 3
print(my_sum(2, 3))
print(my_sum(5, 7))
print(my_sum(25, 39))

def my_sub(num1, num2):
    result = num1 - num2
    return result

print(my_sub(68, 34))
print(my_sub(12, 6))
print(my_sub(15, 66))

def calc_avg(grade1, grade2):
    avg = (grade1 + grade2) / 2
    return avg

print(calc_avg(5, 9))
print(calc_avg(7, 8))